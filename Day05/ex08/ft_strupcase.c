char		*ft_strupcase(char *str)
{
	int		i;
	char	*ptr;

	i = 0;
	ptr = str;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'a') && (str[i] <= 'z'))
			str[i] -= 32;
		i++;
	}
	str = ptr;
	return (str);
}
