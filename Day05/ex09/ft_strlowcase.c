char	*ft_strlowcase(char *str)
{
	int		i;
	char	*ptr;

	ptr = str;
	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'A') && (str[i] <= 'Z'))
			str[i] += 32;
		i++;
	}
	str = ptr;
	return (str);
}
