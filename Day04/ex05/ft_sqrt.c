int		ft_sqrt(int nb)
{
	int i;

	if (nb == 1)
		return (1);
	i = nb - 1;
	while ((i > 1) && (nb < 46341))
	{
		if ((i * i) == nb)
			return (i);
		else
			i--;
	}
	return (0);
}
